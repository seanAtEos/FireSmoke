    var ImgNum = 0;
    var ImgLen = OpsImgs.length - 1;
    var datespec;
    var delay = 300; // Delay between images (msec)
    var lock = false;
    var run;

    function chgImg(direction) {
        if (document.images) {
            ImgNum = ImgNum + direction;
            if (ImgNum > ImgLen) {
                ImgNum = 0;
            }
            if (ImgNum < 0) {
                ImgNum = ImgLen;
            }
            document.getElementById('ops').src = OpsImgs[ImgNum];
            document.getElementById('dev').src = DevImgs[ImgNum];
            datespec = OpsImgs[ImgNum].substr(-14,10);
            document.getElementById('imgtag').innerHTML = datespec;
        }
    }

    function auto() {
        if (lock == true) {
            lock = false;
            document.getElementById('toggle').innerHTML = "Auto";
            window.clearInterval(run);
        }
        else if (lock == false) {
            lock = true;
            document.getElementById('toggle').innerHTML = "Manual";
            run = setInterval("chgImg(1)", delay);
        }
    }

    function firstImg() {
        ImgNum = 0;
        document.getElementById('ops').src = OpsImgs[ImgNum];
        datespec = OpsImgs[ImgNum].substr(-14,10);
        document.getElementById('imgtag').innerHTML = datespec;
    }

    function lastImg() {
        ImgNum = OpsImgs.length - 1;
        document.getElementById('ops').src = OpsImgs[ImgNum];
        datespec = OpsImgs[ImgNum].substr(-14,10);
        document.getElementById('imgtag').innerHTML = datespec;
    }

